lines = input.split("\n").map do |el|
  el.split('')
end

# p lines
p lines[0]

# slope -> right 3, down 1
# O for open square, X for trees
# how many trees do you encounter?
  
repeated_lines = lines.map do |line|
  line * 6
end

# down 1 means changing the index of the larger array
# right 3 means changing the index of the inner array

row = 0
column = 0
tree_count = 0

loop do
  p row # if index < 5
  p column # if index < 5
  if repeated_lines[row][column] == "#"
    tree_count += 1
    repeated_lines[row][column] = 'X'
  else
    repeated_lines[row][column] = 'O'
  end 
  
  row += 1
  column += 3
  break if row == repeated_lines.length
end

p tree_count
